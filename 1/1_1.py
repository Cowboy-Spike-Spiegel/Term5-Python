def tax(salary) -> float:
    if salary <= 60000:
        return 0
    elif 60000 < salary <= 100000:
        return (salary - 60000) * 0.03
    elif 100000 < salary <= 180000:
        return (100000 - 60000) * 0.03 + (salary - 100000) * 0.1
    elif 180000 < salary <= 300000:
        return (100000 - 60000) * 0.03 + (180000 - 100000) * 0.1 + (salary - 180000) * 0.18
    elif 300000 < salary <= 480000:
        return (100000 - 60000) * 0.03 + (180000 - 100000) * 0.1 + (300000 - 180000) * 0.18 + (salary - 300000) * 0.25
    elif 480000 < salary < 700000:
        return (100000 - 60000) * 0.03 + (180000 - 100000) * 0.1 + (300000 - 180000) * 0.18 + (
                    480000 - 300000) * 0.25 + (
                      salary - 480000) * 0.3
    elif 700000 < salary < 1000000:
        return (100000 - 60000) * 0.03 + (180000 - 100000) * 0.1 + (300000 - 180000) * 0.18 + (
                    480000 - 300000) * 0.25 + (
                      700000 - 480000) * 0.3 + (salary - 700000) * 0.35
    elif salary >= 1000000:
        return (100000 - 60000) * 0.03 + (180000 - 100000) * 0.1 + (300000 - 180000) * 0.18 + (
                    480000 - 300000) * 0.25 + (
                      700000 - 480000) * 0.3 + (1000000 - 700000) * 0.35 + (salary - 1000000) * 0.45
    return 0


if __name__ == '__main__':
    salary = float(input())
    print("%.2f" % tax(salary))
